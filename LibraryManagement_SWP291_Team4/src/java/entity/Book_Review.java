package entity;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Book_Review {
    private User user;
    private Date date;
    private int rate;
    private String comment;
    private Book book;

    public Book_Review() {
    }

    public Book_Review(User user, Date date, int rate, String comment, Book book) {
        this.user = user;
        this.date = date;
        this.rate = rate;
        this.comment = comment;
        this.book = book;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }
    
    
}

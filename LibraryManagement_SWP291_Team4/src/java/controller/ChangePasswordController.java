@WebServlet(urlPatterns = {"/changepassword"})
public class ChangePasswordController extends HttpServlet {

    private static final long serialVersionUID = 1L;

    public ChangePasswordController() {
        super();
    }

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        CategoryDAOImpl categoryDAO = new CategoryDAOImpl();
        List<Category> categories = categoryDAO.getAllCategory();
        request.setAttribute("categorylist", categories);
        request.getRequestDispatcher("change-password.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            String oldPassword = (String) request.getParameter("oldPassword");
            String newPassword = (String) request.getParameter("newPassword");
            String reNewPassword = (String) request.getParameter("reNewPassword");
            User u = (User) request.getSession().getAttribute("user");

            CategoryDAOImpl categoryDAO = new CategoryDAOImpl();
            List<Category> categories = categoryDAO.getAllCategory();
            request.setAttribute("categorylist", categories);

            UserDAOImpl userDAO = new UserDAOImpl();
            if (!newPassword.equals(reNewPassword)) {
                request.setAttribute("mess", "Re-enter new password and new password aren't matched");
                request.getRequestDispatcher("change-password.jsp").forward(request, response);
            }
            if (oldPassword.equalsIgnoreCase(newPassword)) {
                request.setAttribute("mess", "New password and old password are the same");
                request.getRequestDispatcher("change-password.jsp").forward(request, response);
            }
            if (u != null && userDAO.check(u.getUsername(), oldPassword) == null) {
                request.setAttribute("mess", "Incorrect old password");
                request.getRequestDispatcher("change-password.jsp").forward(request, response);
            }

            int totalUpdatedUser = userDAO.changePassword(u.getUsername(), newPassword);
            if (totalUpdatedUser < 1) {
                request.setAttribute("mess", "Password changed fail");
                request.getRequestDispatcher("change-password.jsp").forward(request, response);
            } else {
                request.setAttribute("mess", "success");
                request.getRequestDispatcher("change-password.jsp").forward(request, response);
            }
        } catch (Exception e) {
            request.setAttribute("mess", "Internal server error");
            request.getRequestDispatcher("change-password.jsp").forward(request, response);
        }

    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
